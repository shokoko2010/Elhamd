async function updateTataVehicles() {
  try {
    console.log('🚀 بدء تحديث سيارات تاتا موتورز...')

    // حذف السيارات القديمة لتاتا
    await prisma.vehicle.deleteMany({
      where: {
        make: 'Tata'
      }
    })
    console.log('✅ تم حذف سيارات تاتا القديمة')

    // إضافة السيارات الجديدة
    for (const vehicleData of tataVehicles) {
      console.log(`📝 إضافة مركبة: ${vehicleData.title}`)

      // إنشاء المركبة
      const vehicle = await prisma.vehicle.create({
        data: {
          make: 'Tata',
          model: vehicleData.title,
          year: 2024,
          price: 150000, // سعر افتراضي بالريال السعودي
          stockNumber: `TATA-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
          vin: `TATA${Date.now()}${Math.random().toString(36).substr(2, 9).toUpperCase()}`,
          description: vehicleData.description,
          category: vehicleData.category,
          fuelType: 'DIESEL',
          transmission: 'MANUAL',
          mileage: 0,
          color: 'أبيض',
          status: 'AVAILABLE',
          featured: true,
          pricing: {
            create: {
              basePrice: 150000,
              totalPrice: 150000,
              currency: 'SAR'
            }
          }
        }
      })

      console.log(`✅ تم إنشاء المركبة: ${vehicle.id}`)

      // إضافة الصور
      for (let i = 0; i < vehicleData.images.length; i++) {
        await prisma.vehicleImage.create({
          data: {
            vehicleId: vehicle.id,
            imageUrl: `/uploads/vehicles/${vehicleData.images[i]}`,
            altText: `${vehicleData.title} - صورة ${i + 1}`,
            isPrimary: i === 0,
            order: i
          }
        })
      }

      // إضافة المواصفات
      for (const [key, value] of Object.entries(vehicleData.specifications)) {
        await prisma.vehicleSpecification.create({
          data: {
            vehicleId: vehicle.id,
            key: key,
            label: key,
            value: String(value),
            category: 'ENGINE'
          }
        })
      }

      console.log(`✅ تم إضافة الصور والمواصفات لـ: ${vehicleData.title}`)
    }

    console.log('🎉 تم تحديث جميع سيارات تاتا موتورز بنجاح!')
    
    // عرض إحصائيات
    const totalVehicles = await prisma.vehicle.count({
      where: { make: 'Tata' }
    })
    console.log(`📊 إجمالي سيارات تاتا في قاعدة البيانات: ${totalVehicles}`)

    return {
      success: true,
      message: `تم تحديث ${totalVehicles} سيارة تاتا بنجاح`,
      count: totalVehicles
    }

  } catch (error) {
    console.error('❌ خطأ في تحديث سيارات تاتا:', error)
    return {
      success: false,
      message: `فشل في تحديث سيارات تاتا: ${error.message}`,
      error: error.message
    }
  } finally {
    await prisma.$disconnect()
  }
}

module.exports = { updateTataVehicles }